				<h2>About</h2>
				<p>Quickly put, <a href="http://codeigniter.com">CodeIgniter</a> does not have a complete out-of-the-box solution for templating webpages like many other web programming frameworks such as <a href="http://laravel.com/">Laravel</a>, <a href="http://cakephp.com/">CakePHP</a>, <a href="http://fuelphp">FulePHP</a>, or <a href="http://symfony.com/">Symfony</a>.</p>
				<p>There are ton of templates out there, but Stencil is different. Stencil is lightweight, built with usability in mind, and is feature packed. It focuses on keeping things simple and having a natural Codeigniter feel.</p>
				<p>Stencil began as an internal only project, but as coworkers and friends grew to love it, it has since been released open-source and free for anyone to use.</p>
